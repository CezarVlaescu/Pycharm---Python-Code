# Integers - numbers such as 1,2,3
x = 1
print(type(x))
# String - words with '' such as 'hello' ,'hey'
y = "Hello"
print(type(y))
# Float - numbers with . such as 3.4, 2.0
z = 3.40
print(type(z))
# Boolean - True or False
q = False
print(type(q))

# ------------------------------------------------------------------------------------------------------------------------

# List - ordonate/mutable
a = [1, 'hello', 2.3]
print(type(a))
# Tuple - ordonate/immutable
b = ('a', 3, 'hello')
print(type(b))
# Set - neordonate/immutable
c = {'a', 3}
print(type(c))
# Dict - ordonate/mutable { key : value }
d = {'a': 3}
print(type(d))